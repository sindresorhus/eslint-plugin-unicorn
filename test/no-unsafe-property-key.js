import outdent from 'outdent';
import {typescriptEslintParser} from '../scripts/parsers.js';
import {getTester, parsers} from './utils/test.js';

const {test} = getTester(import.meta);

const typeAware = code => ({
	code,
	filename: 'file.ts',
	languageOptions: {
		parser: typescriptEslintParser,
		parserOptions: {projectService: {allowDefaultProject: ['*.ts']}},
	},
});

test.snapshot({
	valid: [
		'object["key"]',
		'object[`key`]',
		'object[key]',
		'object[getKey()]',
		'object[Symbol.iterator]',
		'object[1]',
		'object[0.5]',
		'object[-0.5]',
		'array[4]',
		'object[true]',
		'object[undefined]',
		'object[null]',
		'object[String(key)]',
		'object[array.join("")]',
		'array[Number(4n)]',
		'object[bigint.toString()]',
		'const key = "key"; object[key]',
		'const key = Symbol("key"); object[key]',
		'const key = 1; object[key]',
		'const key = 1.25; object[key]',
		'const key = true; object[key]',
		'let key = {}; object[key]',
		'var key = []; object[key]',
		'const object = {[symbol]: value}',
		'const object = {["key"]: value}',
		'const {[key]: value} = object',
		'class A {[symbol]() {}}',
		'class A {["key"]() {}}',
	],
	invalid: [
		'object[{}]',
		'object[[]]',
		'object[4n]',
		'object[NaN]',
		'object[Infinity]',
		'object[9007199254740992]',
		'object[-9007199254740992]',
		'object[new URL(url)]',
		'object[() => {}]',
		'object[function () {}]',
		'object[class {}]',
		'object?.[{}]',
		'object[{}] = value',
		'delete object[{}]',
		'object[{}]()',
		'const key = {}; object[key]',
		'const key = []; object[key]',
		'const key = 4n; object[key]',
		'const key = Number.NaN; object[key]',
		'const key = Number.POSITIVE_INFINITY; object[key]',
		'const key = 9007199254740992; object[key]',
		'const key = new URL(url); object[key]',
		'const key = () => {}; object[key]',
		'const key = class {}; object[key]',
		'const key = {}; const alias = key; object[alias]',
		'const key = {}; ({[key]: value})',
		'const key = []; class A {[key]() {}}',
		'const object = {[{}]: value}',
		'const object = {[[]]: value}',
		'const object = {[4n]: value}',
		'const object = {[9007199254740992]: value}',
		'const object = {[{}]() {}}',
		'const {[{}]: value} = object',
		'class A {[{}]() {}}',
		'class A {[[]] = value}',
		'class A {[4n]() {}}',
		'class A {[9007199254740992]() {}}',
		'const object = {4n: value}',
		'const object = {9007199254740992: value}',
		'class A {4n() {}}',
		'class A {9007199254740992() {}}',
		outdent`
			const key = {};
			object[key];
		`,
	],
});

test.snapshot({
	testerOptions: {
		languageOptions: {
			parser: parsers.typescript,
		},
	},
	valid: [
		'object[stringKey as string]',
		'object[numberKey as number]',
		'object[symbolKey as symbol]',
		'object[unknownKey as unknown]',
		'const key: {} = "key"; object[key]',
		'interface Key {} const key: Key = "key"; object[key]',
		'const key: number = 1; object[key]',
		'const key: 0.5 = 0.5; object[key]',
		'const key: -0.5 = -0.5; object[key]',
		'const key: boolean = true; object[key]',
	],
	invalid: [
		'object[({} as Foo)]',
		'object[<Foo>{}]',
		'object[([] satisfies Foo)]',
		'object[(4n as const)]',
		'object[(9007199254740992 as const)]',
		'object[({})!]',
		'const key: bigint = 4n; object[key]',
		'const key: object = {}; object[key]',
		'const key: string[] = []; object[key]',
		'const key: [string] = ["key"]; object[key]',
		'const key: () => void = () => {}; object[key]',
		'const key: abstract new () => object = Foo; object[key]',
		'const key: 9007199254740992 = 9007199254740992; object[key]',
		'declare const key: -9007199254740992; object[key]',
		'const key: number = 9007199254740992; object[key]',
		'object[(9007199254740992 as number)]',
		'const key: {} = {}; object[key]',
		'interface Key {} const key: Key = {}; object[key]',
		'class Key {} const key: Key = new Key(); object[key]',
		'const key = {} as Foo; object[key]',
		'const key = [] satisfies Foo; object[key]',
		'abstract class A { abstract [{}]: string }',
		'abstract class A { abstract [{}](): void }',
		'abstract class A { abstract accessor [{}]: string }',
		'abstract class A { abstract 4n(): void }',
	],
});

test.snapshot({
	valid: [
		typeAware('const key: {} = "key"; object[key]'),
		typeAware('interface Key {} const key: Key = "key"; object[key]'),
		typeAware('type Key = string & {brand: true}; declare const key: Key; object[key]'),
	],
	invalid: [
		typeAware('const key = new URL("https://example.com"); object[key]'),
		typeAware('const key = new Date(); object[key]'),
		typeAware('const key = new Map(); object[key]'),
		typeAware('const key: Record<string, unknown> = {}; object[key]'),
		typeAware('const unsafe = 9007199254740992; declare const key: typeof unsafe; object[key]'),
		typeAware('declare const key: ReturnType<() => 9007199254740992>; object[key]'),
	],
});
