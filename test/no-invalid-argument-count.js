test('validates options schema', t => {
	const linter = new Linter({configType: 'flat'});

	const verify = options => linter.verify('foo();', {
		languageOptions: {
			ecmaVersion: 'latest',
			sourceType: 'module',
		},
		plugins: {
			unicorn,
		},
		rules: {
			'unicorn/no-invalid-argument-count': [
				'error',
				options,
			],
		},
	});

	t.throws(
		() => verify({'': 1}),
		{message: /property name '' is invalid/v},
	);
	t.throws(
		() => verify({'new ': 1}),
		{message: /property name 'new ' is invalid/v},
	);
	t.throws(
		() => verify({'foo..bar': 1}),
		{message: /property name 'foo..bar' is invalid/v},
	);
	t.throws(
		() => verify({foo: {min: 3, max: 1}}),
		{message: /`min` must be less than or equal to `max`/v},
	);
});
