import {getTester} from './utils/test.js';

const {test} = getTester(import.meta);

test.snapshot({
	valid: [
		'map.getOrInsert(key, value)',
		'map.getOrInsert(key, "value")',
		'map.getOrInsert(key, 1)',
		'map.getOrInsert(key, [])',
		'map.getOrInsert(key, {})',
		'map.getOrInsert(key, {method() { return tag`value`; }})',
		'map.getOrInsert(key, () => call())',
		'map.getOrInsert(key, () => tag`value`)',
		'map.getOrInsert(key, function () {})',
		'map.getOrInsert(key, function () { return tag`value`; })',
		'map.getOrInsert(key, function (argument = tag`value`) { return argument; })',
		'map.getOrInsert(key, class { field = tag`value`; })',
		// Non-static field initializers run on instantiation, not when the class expression is created.
		'map.getOrInsert(key, class { field = call(); })',
		'map.getOrInsert(key, class { field = new Value(); })',
		'map.getOrInsert(key, class { method() { return call(); } })',
		'map.getOrInsertComputed(key, call())',
		'map.getOrInsert(key)',
		'map.getOrInsert(key, call(), extraArgument)',
		'map.getOrInsert(key, ...call())',
		'map.getOrInsert(...key, call())',
		'map[getOrInsert](key, call())',
		'map["getOrInsert"](key, call())',
		'map.notGetOrInsert(key, call())',
		'getOrInsert(key, call())',
		'new map.getOrInsert(key, call())',
	],
	invalid: [
		'map.getOrInsert(key, call())',
		'map.getOrInsert(key, call(key))',
		'map.getOrInsert(key, new Value())',
		'map.getOrInsert(key, value = call())',
		'map.getOrInsert(key, value++)',
		'map.getOrInsert(key, {value: call()})',
		'map.getOrInsert(key, {value: tag`value`})',
		'map.getOrInsert(key, {...call()})',
		'map.getOrInsert(key, class extends tag`value` {})',
		'map.getOrInsert(key, class { [tag`value`]() {} })',
		'map.getOrInsert(key, class { static field = tag`value`; })',
		'map.getOrInsert(key, class { static { tag`value`; } })',
		'map.getOrInsert(key, tag`value`)',
		'map.getOrInsert(key, (call(), value))',
		'map?.getOrInsert(key, call())',
		'map.getOrInsert?.(key, call())',
		'weakMap.getOrInsert(key, call())',
		'new WeakMap().getOrInsert(key, call())',
		'async function foo() { map.getOrInsert(key, await call()); }',
		'function * foo() { map.getOrInsert(key, yield call()); }',
		'map.getOrInsert(key, eval(key))',
		'map.getOrInsert(key, eval?.(key))',
		'map.getOrInsert(key, call(eval(key)))',
		'map.getOrInsert(key, class extends eval(key) {})',
		'map.getOrInsert(key, class { [eval(key)]() {} })',
		'map.getOrInsert(key, (/* comment */ call()))',
		'map.getOrInsert(key, call(/* comment */ key))',
	],
});
